# Personal Introduction Program
# This program collects user information and displays a welcome message

# Ask user details
name = input("What is your name? ")
age = input("How old are you? ")
city = input("Which city are you from? ")
hobby = input("What is your favorite hobby? ")

# Display welcome message
print("\n🎉 Welcome", name + "! 🎉")
print(f"You are {age} years old and live in {city}.")
print(f"It's great that you love {hobby}!")
print("Nice to meet you. Have a great day! 😊")
